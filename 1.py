''' Definitions
Class: A blueprint or template for creating objects. It defines the attributes (data) and methods (functions) that objects of the class will have.

Object: An instance of a class. It is a concrete realization of the class, with its own unique data values but adhering to the structure defined by the class.

Self: A reference to the instance of the class within its own methods. It is used to access or modify the instance's attributes and call its methods. Note that it isn't necessary to named it self, it is a standard

Constructor: A special method in a class (usually named __init__) that initializes the object's attributes when it is created. It is called automatically when an object is instantiated.

OOP (Object-Oriented Programming): A programming paradigm that organizes code into objects, allowing for encapsulation, inheritance, and polymorphism. It models real-world entities as objects with attributes and behaviors.

Method: A function defined within a class that can perform actions or manipulate the attributes of objects created from that class. Methods define the behavior of objects.

Instance: A specific object created from a class. Each instance has its own set of attribute values but shares the same structure and methods defined in the class.
'''